    {
        //raw query
        //$products = DB::select('SELECT * FROM products');

        //active record
        $products = Product::where('name', 'LIKE', "%{$request->name}%")
                                ->orderBy('name', 'asc')
                                ->get();
        //$products = Product::where('stock_amount', '>', 0)->get();

        return view('products.index', ['products' => $products]);
    }

